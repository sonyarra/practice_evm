# Массив студентов: (Фамилия, Имя, средний балл)
students = [
    ("Иванов", "Пётр", 4.5),
    ("Сидоров", "Алексей", 4.8),
    ("Петров", "Иван", 4.8),
    ("Смирнова", "Анна", 4.2),
]

# Сортируем по убыванию балла и при равенстве — по фамилии по возрастанию
students.sort(key=lambda x: (-x[2], x[0]))

top_n = min(5, len(students))
print(top_n)
for i in range(top_n):
    print(students[i][0], students[i][1], students[i][2])
